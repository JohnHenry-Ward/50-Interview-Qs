def lengthOfLongestSubStringSol1(s):
    if s == '':
        return 0;
    
    currMax = 0;
    possMax = [];
    pointer = 0;
    d = {};
    
    while pointer < len(s):
        for index in range(pointer, len(s)):
            c = s[index];
            if c not in d:
                currMax +=1;
                d[c] = c;
            else:
                d.clear();
                possMax.append(currMax);
                d[c] = c;
                currMax = 1;
        possMax.append(currMax);
        pointer +=1;
        d.clear();
        currMax = 0;
        
    return max(possMax);




